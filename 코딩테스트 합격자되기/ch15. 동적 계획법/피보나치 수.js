/*
문제: 피보나치 수
피보나치 수는 F(0) = 0, F(1) = 1일 때, 1 이상의 n에 대하여 F(n) = F(n-1) + F(n-2)가 적용되는 수입니다.
2 이상의 n이 입력되었을 때, n번째 피보나치 수를 1234567으로 나눈 나머지를 리턴하는 함수를 완성해주세요.

제한 사항:
- n은 2 이상 100,000 이하인 자연수입니다.

입출력 예:
n = 3  → result = 2
n = 5  → result = 5

해결 방법: 동적 계획법 (Dynamic Programming) + 메모이제이션 (Memoization)
- 이전에 계산한 피보나치 수를 배열에 저장하여 중복 계산을 방지
- Bottom-Up 방식으로 작은 문제부터 해결하여 큰 문제를 해결
*/

function solution(n) {
    // 메모이제이션을 위한 배열 초기화
    // fib[0] = 0, fib[1] = 1로 피보나치 수열의 초기값 설정
    const fib = [0, 1];

    // 2번째 피보나치 수부터 n번째까지 계산 (Bottom-Up 방식)
    for (let i = 2; i <= n; i++) {
        // F(n) = F(n-1) + F(n-2) 공식 적용
        // 큰 수의 오버플로우를 방지하기 위해 매 계산마다 1234567로 나눈 나머지 저장
        // 이전에 계산한 값(fib[i-1], fib[i-2])을 재사용하여 효율성 증가
        fib.push((fib[i - 1] + fib[i - 2]) % 1234567);
    }

    // n번째 피보나치 수 반환
    // 배열에 저장된 값을 바로 반환 (O(1) 시간 복잡도)
    return fib[n];
}

// 시간 복잡도: O(n) - n번 반복
// 공간 복잡도: O(n) - n+1개의 피보나치 수를 배열에 저장

console.log(solution(3)); // 2 (0, 1, 1, 2)
console.log(solution(5)); // 5 (0, 1, 1, 2, 3, 5)
