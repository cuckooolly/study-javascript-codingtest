/*
문제: 구명보트
무인도에 갇힌 사람들을 구명보트를 이용하여 구출하려고 합니다.
구명보트는 작아서 한 번에 최대 2명씩 밖에 탈 수 없고, 무게 제한도 있습니다.

예를 들어, 사람들의 몸무게가 [70kg, 50kg, 80kg, 50kg]이고 구명보트의 무게 제한이 100kg이라면
2번째 사람과 4번째 사람은 같이 탈 수 있지만 1번째 사람과 3번째 사람의 무게의 합은 150kg이므로
구명보트의 무게 제한을 초과하여 같이 탈 수 없습니다.

구명보트를 최대한 적게 사용하여 모든 사람을 구출하려고 합니다.
사람들의 몸무게를 담은 배열 people과 구명보트의 무게 제한 limit가 매개변수로 주어질 때,
모든 사람을 구출하기 위해 필요한 구명보트 개수의 최솟값을 return 하도록 solution 함수를 작성해주세요.

제한 사항:
- 무인도에 갇힌 사람은 1명 이상 50,000명 이하입니다.
- 각 사람의 몸무게는 40kg 이상 240kg 이하입니다.
- 구명보트의 무게 제한은 40kg 이상 240kg 이하입니다.
- 구명보트의 무게 제한은 항상 사람들의 몸무게 중 최댓값보다 크게 주어지므로 사람들을 구출할 수 없는 경우는 없습니다.

입출력 예:
people = [70, 50, 80, 50], limit = 100  → result = 3
people = [70, 80, 50], limit = 100  → result = 3

해결 방법: 그리디 알고리즘 (Greedy Algorithm) - 투 포인터 방식
- 가장 가벼운 사람과 가장 무거운 사람을 짝지어 보트에 태우는 전략
- 두 사람의 무게 합이 제한을 넘으면 무거운 사람만 태우고, 넘지 않으면 둘 다 태움
- 정렬 후 양 끝에서 포인터를 이동시키며 최소 보트 개수를 계산
*/

// 교재를 보고 푼 코드
function solution(people, limit) {
    // 사람들의 몸무게를 오름차순으로 정렬
    // 가장 가벼운 사람과 가장 무거운 사람을 짝지어 보트에 태우기 위함
    people.sort((a, b) => a - b);

    // 필요한 구명보트의 개수를 저장하는 변수
    let count = 0;

    // 투 포인터: i는 가장 가벼운 사람의 인덱스(왼쪽 끝)
    let i = 0;

    // 투 포인터: j는 가장 무거운 사람의 인덱스(오른쪽 끝)
    let j = people.length - 1;

    // 모든 사람을 보트에 태울 때까지 반복
    // i와 j가 교차하면 모든 사람이 보트에 탄 것
    while (i <= j) {
        // 가장 가벼운 사람(people[i])과 가장 무거운 사람(people[j])의 무게 합이 제한 이하인지 확인
        if (people[i] + people[j] <= limit) {
            // 두 사람을 함께 태울 수 있으면 가벼운 사람의 포인터를 오른쪽으로 이동
            // (무거운 사람은 아래에서 처리됨)
            i++;
        }

        // 무거운 사람은 항상 보트에 태움 (혼자 타거나 가벼운 사람과 함께)
        // 무거운 사람의 포인터를 왼쪽으로 이동
        j--;

        // 보트 하나를 사용했으므로 카운트 증가
        // (한 명만 타거나 두 명이 함께 타거나 상관없이 보트는 하나씩 사용)
        count++;
    }

    // 필요한 최소 구명보트 개수 반환
    return count;
}

// 시간 복잡도: O(n log n) - 정렬에 O(n log n), 투 포인터 순회에 O(n)
// 공간 복잡도: O(1) - 추가 공간 사용 없음 (정렬은 in-place)

// 테스트
console.log(solution([70,50,80,50],100)); // 3
